/* Generate src/data/generated.ts from the detector's JSON (OAK's embed pattern).
   Python emits data/candidates/index.json; this bakes the per-token view into a
   typed TS module the React app imports at build time. Run via `npm run site:data`. */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const srcPath = path.join(root, "data", "candidates", "index.json");
const outPath = path.join(root, "src", "data", "generated.ts");

if (!fs.existsSync(srcPath)) {
  console.error(`missing ${srcPath} — run \`python3 -m detector.collect\` first`);
  process.exit(1);
}

const data = JSON.parse(fs.readFileSync(srcPath, "utf8"));
// Bake the per-token view (what the UI renders). Per-market detail stays in
// data/snapshots/ for сверка; no need to ship 3.8k rows to the client.
delete data.tokens;

const banner = "/* GENERATED by scripts/build-site-data.mjs — do not edit. */\n";
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, `${banner}export const siteData = ${JSON.stringify(data)};\n`);
console.log(`wrote src/data/generated.ts (${data.token_count} tokens)`);
